package revision.collections.comparator;

public class Cat {

    private Integer id;

    private String name;

    public Cat(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return "Cat " + id + " (" + name + ")";
    }
}
